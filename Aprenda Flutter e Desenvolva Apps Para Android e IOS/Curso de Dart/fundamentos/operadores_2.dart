main() {
  // Operadores Atribuições (binario/infix);

  double a = 2;
  a = a + 2;
  print(a);
  a += 3;
  print(a);
  a -= 3;
  print(a);
  a /= 3;
  print(a);
  a *= 3;
  print(a);
  a %= 2;
  print(a);

  // Operadores relacionais
  print(3 > 2);
  print(3 >= 2);
  print(3 < 2);
  print(3 <= 2);
  print(3 == 2);
  print(3 != 2);
  print(3 == '3');

// faz a comparação bit a bit
  print(5 & 4);
}
